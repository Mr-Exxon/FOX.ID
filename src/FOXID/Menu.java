    private void btn_cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cekActionPerformed
        /*
        Membuat Kode Discount untuk pengguna
        NONTON2020 = 30.000
        NONTONDIFOX = 25.000
        CUMADIFOXID = 40.000
         */
        discount = txt_coupon.getText();

        if (discount.equals("ONLYONFOXID")) {
            txt_disc.setText(String.valueOf("" + 30000));
        } else if (discount.equals("WATCHINGONFOXID")) {
            txt_disc.setText(String.valueOf("" + 25000));
        } else if (discount.equals("HAPPYFOXID")) {
            txt_disc.setText(String.valueOf("" + 40000));
        }
    }//GEN-LAST:event_btn_cekActionPerformed

