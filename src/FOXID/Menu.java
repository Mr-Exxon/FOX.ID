    private void txt_banyakKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_banyakKeyReleased
        /*
        Logika untuk menampilkan harga normal serta harga promo:
        Harga Normal = Harga Tiket * Banyak Tiket
         */
        a = Integer.valueOf(txt_price.getText());
        b = Integer.valueOf(txt_banyak.getText());
        c = (a * b);

        txt_normal.setText("" + c);

        if (c >= 440000) {
            c = (int) (c - (c * 0.15));
            txt_total.setText("" + c);
        } else if (jumlah >= 250000) {
            c = (int) (c - (c * 0.1));
            txt_total.setText("" + c);
        } else if (jumlah >= 170000) {
            c = (int) (c - (c * 0.07));
            txt_total.setText("" + c);
        } else {
            txt_total.setText("" + c);
        }


    }//GEN-LAST:event_txt_banyakKeyReleased

    private void txt_payKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_payKeyReleased
        /*
        Logika untuk menampilkan uang kembalian
        uang kembalian = (uang bayar - total pembelian) + discount
         */
        a = Integer.valueOf(txt_pay.getText());
        b = Integer.valueOf(txt_total.getText());
        d = Integer.valueOf(txt_disc.getText());
        c = d + a - b;
        txt_balance.setText("" + c);
    }//GEN-LAST:event_txt_payKeyReleased

